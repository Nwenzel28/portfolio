(function () {

  if (!document.getElementById("portfolio-container")) return;

  // ---- Build the floating bar ----
  const wrap = document.createElement("div");
  wrap.className = "search-bar-wrap";
  wrap.setAttribute("aria-hidden", "true");

  wrap.innerHTML = `
    <div class="search-bar-inner">
      <span class="search-at-hint">@</span>
      <input
        class="search-input"
        id="search-input"
        type="text"
        autocomplete="off"
        spellcheck="false"
        placeholder="Search students…"
        aria-label="Search portfolios"
      />
      <span class="search-count" id="search-count"></span>
      <span class="search-esc-hint">esc</span>
    </div>
  `;

  document.body.appendChild(wrap);

  const input   = wrap.querySelector(".search-input");
  const countEl = wrap.querySelector(".search-count");
  const inner   = wrap.querySelector(".search-bar-inner");

  // ---- Position below the sticky header ----
  function updateTopOffset() {
    const sticky = document.querySelector(".sticky-top");
    const offset = sticky ? sticky.offsetHeight + 12 : 80;
    wrap.style.setProperty("--search-top", offset + "px");
  }
  updateTopOffset();
  window.addEventListener("resize", updateTopOffset);

  // ---- State ----
  let isOpen = false;

  // ---- Helpers ----
  function getAllItems() {
    return Array.from(document.querySelectorAll("#portfolio-container li"));
  }

  function escapeHtml(str) {
    return str
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;");
  }

  function highlightMatch(name, query) {
    if (!query) return escapeHtml(name);
    const idx = name.toLowerCase().indexOf(query.toLowerCase());
    if (idx === -1) return escapeHtml(name);
    return (
      escapeHtml(name.slice(0, idx)) +
      `<span class="search-highlight">${escapeHtml(name.slice(idx, idx + query.length))}</span>` +
      escapeHtml(name.slice(idx + query.length))
    );
  }

  // Restore every li to its original visible, un-highlighted state
  function restoreAll() {
    getAllItems().forEach(li => {
      li.classList.remove("search-hidden");
      const a = li.querySelector("a");
      if (a) a.innerHTML = escapeHtml(a.dataset.name || a.textContent);
    });
  }

  function updateCount(matched, total, hasQuery) {
    if (!hasQuery) {
      countEl.textContent = `${total}`;
      countEl.classList.remove("no-results");
    } else {
      countEl.textContent = `${matched} / ${total}`;
      countEl.classList.toggle("no-results", matched === 0);
    }
  }

  function filterList(query) {
    const items = getAllItems();
    const total = items.length;
    let matched = 0;

    items.forEach(li => {
      const a = li.querySelector("a");
      if (!a) return;
      // Always read from the stored original name
      const name = a.dataset.name || a.textContent;
      const matches = !query || name.toLowerCase().includes(query.toLowerCase());

      if (matches) {
        li.classList.remove("search-hidden");
        a.innerHTML = highlightMatch(name, query);
        matched++;
      } else {
        li.classList.add("search-hidden");
        a.innerHTML = escapeHtml(name);
      }
    });

    updateCount(matched, total, !!query);
  }

  function openSearch() {
    if (isOpen) return;
    isOpen = true;
    updateTopOffset();
    wrap.classList.add("visible");
    wrap.removeAttribute("aria-hidden");
    input.value = "";
    updateCount(getAllItems().length, getAllItems().length, false);
    requestAnimationFrame(() => input.focus());
  }

  function closeSearch() {
    if (!isOpen) return;
    isOpen = false;
    wrap.classList.remove("visible");
    wrap.setAttribute("aria-hidden", "true");
    input.value = "";
    input.blur();
    restoreAll();
  }

  // ---- Events ----

  // Trigger on '@' when not focused in a text field
  document.addEventListener("keydown", function (e) {
    const tag = document.activeElement.tagName;
    if (tag === "INPUT" || tag === "TEXTAREA") return;
    if (e.key === "@") {
      e.preventDefault();
      openSearch();
    }
  });

  // Live filter; backspace-to-empty closes and restores
  input.addEventListener("input", function () {
    if (input.value === "") {
      closeSearch();
    } else {
      filterList(input.value.trim());
    }
  });

  // Escape closes and restores
  input.addEventListener("keydown", function (e) {
    if (e.key === "Escape") {
      e.stopPropagation();
      closeSearch();
    }
  });

  // Click outside the inner bar closes and restores
  document.addEventListener("click", function (e) {
    if (isOpen && !inner.contains(e.target)) {
      closeSearch();
    }
  });

})();
